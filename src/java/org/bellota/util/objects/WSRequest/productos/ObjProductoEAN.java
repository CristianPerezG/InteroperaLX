package org.bellota.util.objects.WSRequest.productos;

/**
 *
 * @author Cristian Alberto Perez
 */
public class ObjProductoEAN {
    
    private String UEmpaqueEAN;
    private String DescripcionEtiqueta;
    private String DescripcionEtiquetaExtendida;
    private String CodigoEAN;
    private String DescripcionUnidadesEtiqueta;

    public String getUEmpaqueEAN() {
        return UEmpaqueEAN;
    }

    public void setUEmpaqueEAN(String UEmpaqueEAN) {
        this.UEmpaqueEAN = UEmpaqueEAN;
    }

    public String getDescripcionEtiqueta() {
        return DescripcionEtiqueta;
    }

    public void setDescripcionEtiqueta(String DescripcionEtiqueta) {
        this.DescripcionEtiqueta = DescripcionEtiqueta;
    }

    public String getDescripcionEtiquetaExtendida() {
        return DescripcionEtiquetaExtendida;
    }

    public void setDescripcionEtiquetaExtendida(String DescripcionEtiquetaExtendida) {
        this.DescripcionEtiquetaExtendida = DescripcionEtiquetaExtendida;
    }

    public String getCodigoEAN() {
        return CodigoEAN;
    }

    public void setCodigoEAN(String CodigoEAN) {
        this.CodigoEAN = CodigoEAN;
    }

    public String getDescripcionUnidadesEtiqueta() {
        return DescripcionUnidadesEtiqueta;
    }

    public void setDescripcionUnidadesEtiqueta(String DescripcionUnidadesEtiqueta) {
        this.DescripcionUnidadesEtiqueta = DescripcionUnidadesEtiqueta;
    }    
    
}
